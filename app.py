# Импортируем Flask - микрофреймворк для создания веб-серверов
from flask import Flask, render_template

# Создаём экземпляр приложения Flask
app = Flask(__name__)

# Определяем маршрут главной страницы '/'
# Когда пользователь заходит на корневой URL, вызывается эта функция
@app.route('/')
def home():
    # render_template ищет файл index.html в папке templates
    # и возвращает его как готовую веб-страницу
    return render_template('index.html')

# Запускаем сервер, если файл выполняется напрямую (не импортируется)
if __name__ == '__main__':
    # host='0.0.0.0' позволяет подключаться с любого устройства в сети
    # port=5000 — порт, на котором будет работать сервер
    app.run(host='0.0.0.0', port=5000, debug=True)